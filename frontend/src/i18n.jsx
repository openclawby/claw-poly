const DICT = { zh, en }

export const LangCtx = createContext({ lang: 'zh', setLang: () => {}, t: (k) => k })

export function LangProvider({ children }) {
  const [lang, setLangRaw] = useState(localStorage.getItem('pmbot_lang') || 'zh')
  const value = useMemo(() => ({
    lang,
    setLang: (l) => { localStorage.setItem('pmbot_lang', l); setLangRaw(l) },
    t: (k, vars) => {
      let s = DICT[lang]?.[k] ?? DICT.zh[k] ?? k
      if (vars) Object.entries(vars).forEach(([a, b]) => { s = s.replaceAll(`{${a}}`, b) })
      return s
    },
  }), [lang])
  return <LangCtx.Provider value={value}>{children}</LangCtx.Provider>
}

export const useLang = () => useContext(LangCtx)

// backend META bilingual field picker: mx(lang, meta, 'name') -> name / name_en
export const mx = (lang, obj, field) =>
  (lang === 'en' ? obj?.[`${field}_en`] ?? obj?.[field] : obj?.[field])
